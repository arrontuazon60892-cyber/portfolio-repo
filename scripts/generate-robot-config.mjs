import { mkdir, readdir, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const modelsDirectory = path.join(root, "public", "models");
const outputPath = path.join(root, "src", "config", "robot.generated.js");
let modelUrl = null;

try {
  const files = await readdir(modelsDirectory, { withFileTypes: true });
  const exactModel = files.find((entry) => entry.isFile() && entry.name === "robot.glb");
  if (exactModel) modelUrl = "/models/robot.glb";
  else {
    const caseMismatch = files.find((entry) => entry.isFile() && entry.name.toLowerCase() === "robot.glb");
    if (caseMismatch) console.warn(`Robot model ignored: expected robot.glb but found ${caseMismatch.name}. Vercel paths are case-sensitive.`);
  }
} catch (error) {
  if (error.code !== "ENOENT") throw error;
}

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, `// Generated by scripts/generate-robot-config.mjs.\nexport const ROBOT_MODEL_URL = ${JSON.stringify(modelUrl)};\n`, "utf8");
console.log(modelUrl ? `Robot model: ${modelUrl}` : "Robot model: not found (optional fallback active)");
